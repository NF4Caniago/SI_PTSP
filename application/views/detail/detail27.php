        <h2>Prosedur</h2>
        <ol>
            <li>Pemohon melengkapi berkas yang dibutuhkan;</li>
            <li>Pemohon memasukkan Berkas ke Bagian Umum;</li>
            <li>Berkas dinaikkan ke kakanwil untuk diberikan Disposisi Pimpinan;</li>
            <li>Kepala Bidang PHU cq. Kasi Pembinaan Haji dan Umrah cq. Tim setelah mendapat disposisi dan surat tugas melakukan verifikasi berkas permohonan;</li>
            <li>Hasil verifikasi dituangkan dalam Berita Acara Hasil Verifikasi berkas dan Lembar Verifikasi Persyaratan;</li>
            <li>Setelah lolos verifikasi berkas dinyatakan memenuhi syarat, akan dilakukan peninjauan lapangan oleh Tim dengan menuangkan hasilnya dalam Berita Acara Hasil Peninjauan Lapangan;</li>
            <li>Hasil Verifikasi Berkas dan Peninjauan Lapangan ditelaah oleh Kepala Bidang PHU bila dinyatakan layak dan memenuhi syarat dibuatkan draf Surat Rekomendasi izin pendirian PPIU;</li>
            <li>Kepala Kanwil Kemenag DIY akan memberikan rekomendasi izin pendirian PPIU kepada pemohon yang memenuhi syarat dengan mempertimbangkan hasil verifikasi (chek list persyaratan) dan berita acara hasil peninjauan lapangan dan akan menolak memberikan rekomendasi bagi yang tidak memenuhi syarat dengan penjelasan tertulis; dan</li>
            <li>Pemohon yang mendapat rekomendasi selanjutnya memproses permohonan izin pendirian PPIU kepada Menteri Agama RI dengan dilampiri persyaratan yang tersusun secara tertib dan berurutan.</li>
        </ol>
