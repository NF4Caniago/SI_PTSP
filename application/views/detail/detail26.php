        <h2>Prosedur</h2>
        <ol>
            <li>Pemohon melengkapi berkas yang dibutuhkan;</li>
            <li>Pemohon memasukkan Berkas ke Sub Bagian Umum;</li>
            <li>Berkas dinaikkan ke kakanwil untuk diberikan Disposisi Pimpinan;
            <li>Kepala Bidang PHU cq. Kasi Pembinaan Haji dan Umrah cq. Tim setelah mendapat disposisi dan surat tugas melakukan verifikasi berkas permohonan;</li>
            <li>Hasil verifikasi dituangkan dalam Berita Acara Hasil Verifikasi berkas dan Lembar Verifikasi Persyaratan;</li>
            <li>Setelah lolos verifikasi berkas dinyatakan memenuhi syarat, akan dilakukan peninjauan lapangan oleh Tim dengan menuangkan hasilnya dalam Berita Acara Hasil Peninjauan Lapangan;</li>
            <li>Hasil Verifikasi Berkas dan Peninjauan Lapangan ditelaah oleh Kepala Bidang PHU bila dinyatakan layak dan memenuhi syarat dibuatkan draf Surat Keputusan Persetujuan Kantor Cabang PIHK dan/atau Surat Keputusan Pengesahan Kantor Cabang PPIU;</li>
            <li>Kepala Kanwil Kemenag DIY akan memberikan Persetujuan Pembukaan Kantor Cabang PIHK dan/atau Pengesahan Pembukaan Kantor Cabang PPIU kepada pemohon yang memenuhi syarat dengan mempertimbangkan hasil verifikasi (chek list persyaratan) dan berita acara hasil peninjauan lapangan dan akan menolak memberikan persetujuan dan/atau pengesahan bagi yang tidak memenuhi syarat dengan penjelasan tertulis; dan </li>
            <li>Kantor Cabang PIHK / PPIU yang mendapat persetujuan dan/atau pengesahan melaporkan secara tertulis kepada kepada Dirjen Penyelenggaraan Haji dan Umrah Kemenag RI (pusat) lewat Pimpinan Pusat PIHK / PPIU yang bersangkutan dengan dilampiri persyaratan yang tersusun secara tertib dan berurutan.</li>
        </ol>
